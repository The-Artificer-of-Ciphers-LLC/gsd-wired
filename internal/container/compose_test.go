// TestWriteComposeFragment_RefusesOverwrite verifies force=false returns error when file exists.
func TestWriteComposeFragment_RefusesOverwrite(t *testing.T) {
	dir := t.TempDir()

	// First write succeeds.
	_, err := container.WriteComposeFragment(dir, testCfg(), false)
	if err != nil {
		t.Fatalf("first WriteComposeFragment: %v", err)
	}

	// Second write without force should fail.
	_, err = container.WriteComposeFragment(dir, testCfg(), false)
	if err == nil {
		t.Fatal("expected error when overwriting without force, got nil")
	}
	if !strings.Contains(err.Error(), "already exists") {
		t.Errorf("error %q should mention 'already exists'", err.Error())
	}
	if !strings.Contains(err.Error(), "--force") {
		t.Errorf("error %q should mention '--force'", err.Error())
	}
}

// TestWriteComposeFragment_ForceOverwrite verifies force=true allows overwriting.
func TestWriteComposeFragment_ForceOverwrite(t *testing.T) {
	dir := t.TempDir()

	_, err := container.WriteComposeFragment(dir, testCfg(), false)
	if err != nil {
		t.Fatalf("first WriteComposeFragment: %v", err)
	}

	// Second write with force=true should succeed.
	_, err = container.WriteComposeFragment(dir, testCfg(), true)
	if err != nil {
		t.Fatalf("WriteComposeFragment with force: unexpected error: %v", err)
	}
}

