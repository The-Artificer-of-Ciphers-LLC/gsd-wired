package container_test

import (
	"os"
	"path/filepath"
	"strings"
	"testing"

	"github.com/The-Artificer-of-Ciphers-LLC/gsd-wired/internal/container"
)

func testCfg() container.ContainerConfig {
	return container.ContainerConfig{
		BeadsDoltDir: ".beads/dolt",
		HostPort:     "3307",
	}
}

// TestWriteComposeFragment_WritesValidYAML verifies the compose fragment is written correctly.
func TestWriteComposeFragment_WritesValidYAML(t *testing.T) {
	dir := t.TempDir()
	path, err := container.WriteComposeFragment(dir, testCfg(), false)
	if err != nil {
		t.Fatalf("WriteComposeFragment: unexpected error: %v", err)
	}

	if filepath.Base(path) != "gsdw.compose.yaml" {
		t.Errorf("expected filename 'gsdw.compose.yaml', got %q", filepath.Base(path))
	}

	data, err := os.ReadFile(path)
	if err != nil {
		t.Fatalf("ReadFile: %v", err)
	}
	content := string(data)

	checks := []string{
		"gsdw-dolt",
		"dolthub/dolt-sql-server:latest",
		"127.0.0.1:3307:3306",
		".beads/dolt:/var/lib/dolt",
		"DOLT_ROOT_HOST",
		"unless-stopped",
	}
	for _, want := range checks {
		if !strings.Contains(content, want) {
			t.Errorf("compose YAML missing %q\ncontent:\n%s", want, content)
		}
	}
}

// TestWriteComposeFragment_CommentHeader verifies the header comment is present.
